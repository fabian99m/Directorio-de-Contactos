    private void listaConMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listaConMouseClicked

        if (modBus.isEnabled() || directorio.EstaVacio()) {
            if (modBus.isEnabled()) {
                JOptionPane.showMessageDialog(this, "Seleccione modo de búsqueda!.");
            } else {
                JOptionPane.showMessageDialog(this, "Directorio de contactos vacío!.");
            }
        } else {
            String nombre = listaCon.getSelectedValue().toString();
            if (directorio.buscarContacto(nombre) != null) {
                Contacto aux = directorio.buscarContacto(nombre);
                txtNombre.setText(aux.getNombre());
                txtTele.setText(aux.getTelefono());
                txtCorr.setText(aux.getCorreo());
                txtDire.setText(aux.getDireccion());
            }
        }

    }//GEN-LAST:event_listaConMouseClicked

