    private void agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarActionPerformed
        
        if (!modIns.isEnabled()) {
            if (txtNombre.getText().isEmpty() || txtNombre.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Ingrese un nombre!.");
            } else {
                directorio.AgregarContacto(txtNombre.getText(), txtTele.getText(), txtDire.getText(), txtCorr.getText());
                Listar();
                txtNombre.setText(null);
                txtTele.setText(null);
                txtCorr.setText(null);
                txtDire.setText(null);
                taman.setText(Integer.toString(directorio.getTama()));
            }
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione el modo insercción!");
        }

    }//GEN-LAST:event_agregarActionPerformed

